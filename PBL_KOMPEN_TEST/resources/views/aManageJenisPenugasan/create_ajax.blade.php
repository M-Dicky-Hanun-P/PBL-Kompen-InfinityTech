        <div class="modal-content">
            <div class="modal-header">
                <h5 class="modal-title">Tambah Data Jenis Penugasan</h5>
                <button type="button" class="close" data-dismiss="modal" aria-label="Close">
                    <span aria-hidden="true">&times;</span>
                </button>
            </div>
            <div class="modal-body">
                <div class="form-group">
                    <label for="jenis_tugas">Jenis Penugasan</label>
                    <input type="text" name="jenis_tugas" id="jenis_tugas" class="form-control" required>
                    <small id="error-jenis_tugas" class="form-text text-danger"></small>
                </div>
